class Layout:
    def __init__(self, layout_orientation: str, base: str, device_layout: List[Any] = []):
        self.layout_orientation = layout_orientation
        self.base = base
        self.device_layout = device_layout
        self.number_modules = len(device_layout)
        self.image_draw_flipped = False
        self.rotation_degrees = self.get_rotation_degrees()

    def get_rotation_degrees(self) -> int:
        if self.layout_orientation == "vertical":return 180 if self.base == "bottom" else 0
        return 270 if self.base == "left" else 90

    def get_raw_rgb_data(self) -> str:
        return "".join(module.get_rgb_data() for module in self.device_layout)

